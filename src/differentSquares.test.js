const solution = require('./differentSquares.js');

test('test 1', () => {
    expect(solution([[1,2,1], 
        [2,2,2], 
        [2,2,2], 
        [1,2,3], 
        [2,2,1]])).toBe(6);
});

test('test 2', () => {
    expect(solution([[9,9,9,9,9], 
        [9,9,9,9,9], 
        [9,9,9,9,9], 
        [9,9,9,9,9], 
        [9,9,9,9,9], 
        [9,9,9,9,9]])).toBe(1);
});

test('test 3', () => {
    expect(solution([[3]])).toBe(0);
});

test('test 4', () => {
    expect(solution([[3,4,5,6,7,8,9]])).toBe(0);
});

test('test 5', () => {
    expect(solution([[3], 
        [4], 
        [5], 
        [6], 
        [7]])).toBe(0);
});

test('test 6', () => {
    expect(solution([[2,5,3,4,3,1,3,2], 
        [4,5,4,1,2,4,1,3], 
        [1,1,2,1,4,1,1,5], 
        [1,3,4,2,3,4,2,4], 
        [1,5,5,2,1,3,1,1], 
        [1,2,3,3,5,1,2,4], 
        [3,1,4,4,4,1,5,5], 
        [5,1,3,3,1,5,3,5], 
        [5,4,4,3,5,4,4,4]])).toBe(54);
});

test('test 7', () => {
    expect(solution([[1,1,1,1,1,1,2,2,2,3,3,3,9,9,9,2,3,9]])).toBe(0);
});
